//TO TRANSFER WORD DATA FROM A TO B
//TO TRANSFER HALF WORD DATA FORM C TO D
//TO TRANSFER BYTE DATA FORM E TO F
.DATA
A: .WORD 1,2,3,4,5
B: .WORD 0,0,0,0,0
C: .HWORD 1,2,3,4,5
D: .HWORD 0,0,0,0,0
E: .BYTE 1,2,3,4,5
F: .BYTE 0,0,0,0,0
.TEXT
//MOVING THE WORD DATA
LDR R0,=A
LDR R1,=B
MOV R10,#0 //COUNT REG
LOOP1:
LDR R2,[R0]
STR R2,[R1]
ADD R0,R0,#4
ADD R1,R1,#4
ADD R10,R10,#1
CMP R10,#5
BNE LOOP1
//MOVING THE HALF WORD DATA
LDR R0,=C
LDR R1,=D
MOV R10,#0 //COUNT REG
LOOP2:
LDR R2,[R0]
STR R2,[R1]
ADD R0,R0,#4
ADD R1,R1,#4
ADD R10,R10,#1
CMP R10,#5
BNE LOOP2
//MOVING THE BYTE DATA
LDR R0,=E
LDR R1,=F
MOV R10,#0 //COUNT REG
LOOP3:
LDR R2,[R0]
STR R2,[R1]
ADD R0,R0,#4
ADD R1,R1,#4
ADD R10,R10,#1
CMP R10,#5
BNE LOOP3
	